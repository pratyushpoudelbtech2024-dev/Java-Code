public class divide {
    public static void main(String[] args) {
        double dividend = 10;
        double divisor = 0;
        
        if (divisor == 0) {
            System.out.println("Error: Division by zero is not allowed.");
        } else {
            double result = dividend / divisor;
            System.out.println("The result of division is: " + result);
        }
    }
}
